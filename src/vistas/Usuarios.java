    //llenar tabla Usuarios
    private static void llenarTablaUsuarios() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            List<Usuario> usuarios = new UsuarioDAOImpl().listar(labelDepartamento.getText());

            String[] columnas = {"ID", "Nombre(s)", "Apellidos", "Usuario", "Departamento", "Rol"};
            modelo.setColumnIdentifiers(columnas);

            for (Usuario usuario : usuarios) {
                String[] renglon = {String.valueOf(usuario.getIdUsuario()), usuario.getNombre(), usuario.getApellidos(), usuario.getUsuario(),
                    usuario.getDepartamento(), usuario.getRol()};
                modelo.addRow(renglon);
            }
            tablaUsuarios.setModel(modelo);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

