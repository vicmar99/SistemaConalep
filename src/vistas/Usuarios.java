    private void btnAceptarActModUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActModUsuarioActionPerformed
        String nombre = txtNombreUsuario.getText();
        String apellidos = txtApellidos.getText();
        String nusuario = txtUsuarioUsuario.getText();
        String clave = txtClaveUsuario.getText();
        String departamento = labelDepartamento.getText();
        String rol = boxRolUsuario.getSelectedItem().toString();

        if (nombre.trim().isEmpty() || apellidos.trim().isEmpty() || nusuario.trim().isEmpty()
                || clave.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos",
                    "AVISO", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Usuario usuario = new Usuario();
        usuario.setIdUsuario(idUsuario);
        usuario.setNombre(nombre);
        usuario.setApellidos(apellidos);
        usuario.setUsuario(nusuario);
        usuario.setClave(clave);
        usuario.setDepartamento(departamento);
        usuario.setRol(rol);

        try {
            UsuarioDAO usuarioDAO = new UsuarioDAOImpl();
            Usuario usuarioBuscado = usuarioDAO.traerUnUsuario(String.valueOf(idUsuario));
            if (usuarioBuscado.getIdUsuario() == 0) {
                usuarioDAO.registrar(usuario);
                JOptionPane.showMessageDialog(null, "OperaciÃ³n exitosa", "AVISO",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                usuarioDAO.modificar(usuario);
                JOptionPane.showMessageDialog(null, "OperaciÃ³n exitosa", "AVISO",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "OcurriÃ³ un error al realizar la opeaciÃ³n",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        }

        llenarTablaUsuarios();
        txtNombreUsuario.setText("");
        txtApellidos.setText("");
        txtUsuarioUsuario.setText("");
        txtClaveUsuario.setText("");
        deshabilitarPanelNuevoUsuario();
        idUsuario = -1;
    }//GEN-LAST:event_btnAceptarActModUsuarioActionPerformed

