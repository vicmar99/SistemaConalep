    private void btnModificarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarUsuarioActionPerformed
        int fila = tablaUsuarios.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar por lo menos una fila para realizar la operaciÃ³n", "AVISO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String idUsuarioM = String.valueOf(tablaUsuarios.getValueAt(fila, 0));
        idUsuario = Integer.parseInt(idUsuarioM);
        UsuarioDAO usuarioDAO = new UsuarioDAOImpl();

        try {
            Usuario usuario = usuarioDAO.traerUnUsuario(idUsuarioM);
            txtNombreUsuario.setText(usuario.getNombre());
            txtApellidos.setText(usuario.getApellidos());
            txtUsuarioUsuario.setText(usuario.getUsuario());
            txtClaveUsuario.setText(usuario.getClave());
            boxRolUsuario.setSelectedItem(usuario.getRol());
        } catch (Exception e) {
            e.printStackTrace();
        }
        habilitarPanelNuevoUsuario();
    }//GEN-LAST:event_btnModificarUsuarioActionPerformed

    private void btnEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUsuarioActionPerformed
        int fila = tablaUsuarios.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar por lo menos una fila para realizar la operaciÃ³n",
                    "AVISO", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int respuesta = JOptionPane.showConfirmDialog(null, "Los registro seleccionados se eliminarÃ¡n de forma definitiva\nÂ¿Desea continuar?",
                "Confirmar eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (respuesta == JOptionPane.YES_OPTION) {

            UsuarioDAO usuarioDAO = new UsuarioDAOImpl();

            for (int i : tablaUsuarios.getSelectedRows()) {
                try {
                    Usuario usuario = usuarioDAO.traerUnUsuario(String.valueOf(tablaUsuarios.getValueAt(i, 0)));
                    if (usuario.getRol().equalsIgnoreCase("Admin")) {
                        JOptionPane.showMessageDialog(null, "No se puede realizar la operaciÃ³n\n"
                                + "Un usuario de tipo administrador  no se puede eliminar", "AVISO", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    usuarioDAO.eliminar(String.valueOf(tablaUsuarios.getValueAt(i, 0)));
                    JOptionPane.showMessageDialog(null, "OperaciÃ³n exitosa", "AVISO", JOptionPane.INFORMATION_MESSAGE);

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            llenarTablaUsuarios();
        }
    }//GEN-LAST:event_btnEliminarUsuarioActionPerformed


